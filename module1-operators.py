print(2 + 3)
print(5 - 4)
print(5 * 4)

# / Standard division, they always return float outputs
print(6 / 2)
print(7 / 2)

# // integer division, they always return rounded outputs (lowest whole number)
print(6 // 2)
print(7 // 2)

# % modulus division, returns the remainder of the division
print(6 % 2)
print(7 % 2)
print(11 % 2)
print(22 % 2)

# ** power operator
print(2 ** 3)